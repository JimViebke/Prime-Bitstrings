	consteval uint32_t cached_hitcount_for(const auto base, const auto prime)
	{
		// Kind of icky compile-time linear search
		for (const auto& r : detail::records)
		{
			if (r.base == base && r.prime == prime)
			{
				return r.hits;
			}
		}

		return uint32_t(-1);
	}

